SELECT CLINOM AS CLIENTE,DHORA AS FECHA,DALMACEN AS ALMACEN,

DPAGO1,DPAGO2,DPAGO3,DPAGO1+DPAGO2+DPAGO3 AS TOTAL,

IF(DPAGO2<>0,1,'') AS TIPO_TARJETA,

IEAN AS EAN,

ICOD AS CODIGO_PRODUCTO,

UABREGEN AS UNIDAD,

AICANTF AS PIEZAS,

AIPRECIO AS PRECIO,

AIDESCTO AS DESCUENTO

FROM FAXINV

LEFT JOIN FDOC ON FDOC.DSEQ=FAXINV.DSEQ

LEFT JOIN FINV ON FINV.ISEQ=FAXINV.ISEQ

LEFT JOIN FCLI ON FCLI.CLISEQ=FDOC.CLISEQ

LEFT JOIN FUNIDAD ON FUNIDAD.UCOD=FINV.IUM

WHERE DITIPMV='T' AND DFECHA>='2019-11-06' AND DFECHA<='2019-11-06'